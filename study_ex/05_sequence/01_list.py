"""
리스트(list)는 파이썬에서 가장 많이 사용되는 타입 / 문자열과 같이 시퀀스 타입에 속함.
시퀀스 타입의 공통적인 특징은 순서 있게 객체를 내포
"""

my_list = ['p', 'y', 't', 'h', 'o', 'n']  # list 타입 생성

print(my_list[0])  # 출력: p
print(my_list[1])  # 출력: y
print(my_list[-1])  # 출력: n

my_str = "python"  # 문자열 타입

print(my_str[0])  # 출력: p
print(my_str[-1])  # 출력: n
